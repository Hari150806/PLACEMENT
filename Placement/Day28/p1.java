package Placement.Day28;

public class p1 { // flip last bit
    public static void main(String[] args) {
        int n = 5; // Example number
        int result = n ^ 1; // Flip the last bit
        System.out.println("Original number: " + n);
        System.out.println("Number after flipping last bit: " + result);
    }
    
}
